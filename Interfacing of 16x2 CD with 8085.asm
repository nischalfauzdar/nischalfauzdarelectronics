ORG 0000H
/*0X38H ;INITIALIZIING LCD 2 LINE	5/7 MATRIX
0EH ;DISPLAY ON CURSER ON
01H ;CLEAR LCD
06H ;SHIFT CURSER RIGHT
80H ;START DISPLAY FROM 0TH ROW 
0C0H ;PRINTING FROM SECOND LINE 0TH POSITION
	*/
RS BIT P2.5
RW BIT P2.6
EN BIT P2.7
MOV A, #38H
ACALL COMMORT

MOV A, #OEH
ACALL COMMORT

MOV A, #01H
ACALL COMMORT

MOV A,#06H
ACALL COMMORT

MOV A, #80H
ACALL COMMORT


COMMWRT: ACALL DELAY
		CLR RS
		CLR RW
		SETB EN
		MOV P1,A
		CLR EN
RET

DELAY: MOV R1,#0FFH
HERE: MOV R0,#0FFH
HERE1: DJNZ R0,HERE1
DJNZ R1,HERE
RET

MOV A, #'N'
ACALL DATAWRT
MOV A, #'I'
ACALL DATAWRT
MOV A, #'S'
ACALL DATAWRT
MOV A, #'C'
ACALL DATAWRT
MOV A, #'H'
ACALL DATAWRT
MOV A, #'A'
ACALL DATAWRT
MOV A, #'L'
ACALL DATAWRT
DATAWRT: ACALL DELAY
SETB RS
CLR RW
SETB EN
MOV P1,A
CLR EN
RET

MOV A, #OCO
ACALL COMMWRT
MOV A, #'F'
ACALL DATAWRT
MOV A, #'A'
ACALL DATAWRT
MOV A, #'U'
ACALL DATAWRT
MOV A, #'Z'
ACALL DATAWRT
MOV A, #'D'
ACALL DATAWRT
MOV A, #'A'
ACALL DATAWRT
MOV A, #'R'
ACALL DATAWRT

DATAWRT: ACALL DELAY
SETB RS
CLR RW
SETB EN
MOV P1,A
CLR EN
RET