ORG 0000H
/*0X38H ;INITIALIZIING LCD 2 LINE	5/7 MATRIX
0EH ;DISPLAY ON CURSER ON
01H ;CLEAR LCD
06H ;SHIFT CURSER RIGHT
80H ;START DISPLAY FROM 0TH ROW 
0C0H ;PRINTING FROM SECOND LINE 0TH POSITION
LM35 TEMEPRATURE SENSOR
ADC0804	ANALOG DATA TO DIGITAL DATA CONVERTER
	*/

RS BIT P2.5
RW BIT P2.6
EN BIT P2.7
MOV A, #38H
ACALL COMMORT

MOV A, #0EH
ACALL COMMORT

MOV A, #01H
ACALL COMMORT

MOV A,#06H
ACALL COMMORT

MOV A, #80H
ACALL COMMORT

MOV A, #'T'
ACALL DATAWRT
MOV A, #'E'
ACALL DATAWRT
MOV A, #'M'
ACALL DATAWRT
MOV A, #'P'
ACALL DATAWRT
MOV A, #':-'
ACALL DATAWRT
MOV A, #' '
ACALL DATAWRT

COMMORT: ACALL DELAY
		CLR RS
		CLR RW
		SETB EN
		MOV P1,A
		CLR EN
RET

DELAY: MOV R1,#0FFH
HERE: MOV R0,#0FFH
HERE1: DJNZ R0,HERE1
DJNZ R1,HERE
RET


DATAWRT: ACALL DELAY
SETB RS
CLR RW
SETB EN
MOV P1,A
CLR EN
RET

END